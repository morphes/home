		<p><strong>Тема 16-го Зимнего фестиваля «Золотая капитель»</strong><br>
		«Устойчивая архитектура. Устойчивый город. Устойчивая территория»</p>
		<p><strong>Организатор фестиваля</strong><br>
		Фонд «Современная архитектура» при содействии ITE «Сибирская Ярмарка» <br>
		и Новосибирского Союза архитекторов России</p>
		<p><strong>Место проведения фестиваля</strong><br>
		«Новосибирск Экспоцентр»</p>
		<strong>Сроки проведения фестиваля</strong>
		<ul>
			<li>До 31 января 2012. Формирование электронной и выставочной экспозиций, судейство 1 этапа смотра-конкурса,<br>формирование шорт-листа смотра-конкурса.</li>
			<li>31 января 2012 в 17:00. Открытие смотра-конкурса (выставочной и интернет-экспозиций).</li>
			<li>3 февраля 2012 в 17:00. Официальная церемония объявления итогов и награждение победителей.</li>
		</ul>
		<p>Для победителей в различных номинациях смотра-конкурса предусмотрены обновленные статуэтки «Золотая капитель», дипломы, денежные вознаграждения и специальные призы от организаторов, партнеров и спонсоров фестиваля. </p>
		<strong>Для участия в фестивале «Золотая капитель» потребуется</strong>
		<ol>
			<li>Текст с описанием проекта не более 1 тыс. знаков в формате DOC, RTF.</li>
			<li>Один или два выставочных листа высотой 113 см, шириной 96 см – в формате TIF или JPEG с разрешением не менее 200 dpi.</li>
			<li>Иллюстративные изображения, представленные на планшете – отдельными файлами в формате TIF<br>с разрешением 300 dpi и длиной  большей стороны не менее 2000 pix.</li>
		</ol>
